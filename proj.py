class Projetos:

    def __init__(self, ordem_de_vendaProva, tipo, documentos, cliente, gerente_projetos, gerente_conta, status):
        self.__ordem_de_vendaProva = ordem_de_vendaProva
        self.__tipo = tipo
        self.__documentos = documentos
        self.__cliente = cliente
        self.__gerente_projetos = gerente_projetos
        self.__gerente_conta = gerente_conta
        self.__status = status


    def dados(self, ordem_de_vendaProva, tipo, documentos, cliente, gerente_projetos, gerente_conta, status):
        self.__ordem_de_vendaProva = ordem_de_vendaProva
        self.__tipo = tipo
        self.__documentos = documentos
        self.__cliente = cliente
        self.__gerente_projetos = gerente_projetos
        self.__gerente_conta = gerente_conta
        self.__status = status


